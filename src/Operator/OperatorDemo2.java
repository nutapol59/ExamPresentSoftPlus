public class OperatorDemo2 {
    public static void main(String[] args){
//        The Relational Operators

        int a = 20, b = 20;

        if (a == b){
            System.out.println("a == b");
        }

        if (a != b){
            System.out.println("a != b");
        }

        if (a > b){
            System.out.println("a > b");
        }

        if (a < b){
            System.out.println("a < b");
        }

        if (a >= b){
            System.out.println("a >= b");
        }

        if (a <= b){
            System.out.println("a <= b");
        }
    }
}
